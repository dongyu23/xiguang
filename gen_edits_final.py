# ── Main logic ─────────────────────────────────────────────────────
def main():
    print(f"Reading detail.json from: {DETAIL_PATH}")
    with open(DETAIL_PATH, "r", encoding="utf-8") as f:
        detail = json.load(f)

    template_slug = detail.get("slug", TEMPLATE_SLUG)
    pages = detail["pages"]

    # Build lookup: slide_number -> page data
    page_lookup = {p["slide_number"]: p for p in pages}

    edits = []

    for slide_idx, slide_num in enumerate(SELECTED_SLIDES):
        ppt_page = slide_idx + 1  # PPT page number (1-30)

        if slide_num not in page_lookup:
            print(f"  WARNING: Slide {slide_num} not found in template, skipping.")
            continue

        page = page_lookup[slide_num]
        content_map = CONTENT.get(slide_num, {})

        for slot in page["text_slots"]:
            slot_id = slot["slot_id"]
            current_text = slot.get("current_text", "")

            # Skip non-editable slots
            if not slot.get("editable", True):
                continue

            # Skip copyright marks
            if current_text == "©方能演亦":
                continue

            # Check if we have content defined for this slot
            if slot_id in content_map:
                new_text = content_map[slot_id]
                max_chars = slot.get("max_chars", 0)

                # Truncate if exceeds max_chars
                if max_chars > 0 and len(new_text) > max_chars:
                    # Try to truncate cleanly at a sentence boundary
                    truncated = new_text[:max_chars]
                    if len(new_text) > max_chars:
                        truncated = new_text[:max_chars]
                    new_text = truncated

                edits.append({
                    "slide_number": slide_num,
                    "slot_id": slot_id,
                    "text": new_text
                })
            else:
                # Slot exists but we don't have content for it - skip silently
                # (many slots are decorative or not relevant to our topic)
                pass

    # Build output
    output = {
        "template_slug": template_slug,
        "selected_slides": SELECTED_SLIDES,
        "edits": edits
    }

    # Write output
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"\nDone! Generated {len(edits)} edits for {len(SELECTED_SLIDES)} slides.")
    print(f"Output written to: {OUTPUT_PATH}")

    # Summary stats
    slides_covered = len(set(e["slide_number"] for e in edits))
    print(f"Slides with content: {slides_covered}/{len(SELECTED_SLIDES)}")

    # Show per-slide edit counts
    from collections import Counter
    slide_counts = Counter(e["slide_number"] for e in edits)
    for slide_num in SELECTED_SLIDES:
        idx = SELECTED_SLIDES.index(slide_num) + 1
        count = slide_counts.get(slide_num, 0)
        print(f"  PPT {idx:2d} (Slide {slide_num:2d}): {count:3d} edits")


if __name__ == "__main__":
    main()
